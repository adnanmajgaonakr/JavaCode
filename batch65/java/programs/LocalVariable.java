package batch65.java.programs;

public class LocalVariable {

	public static void main(String[] args) {
		
		int number = 20;
		float f1 = 53.56f;
		
		System.out.println("The value of local variable of type int is : " + number);
		System.out.println("The value of local variable of type float is : " + f1);
	}
}
