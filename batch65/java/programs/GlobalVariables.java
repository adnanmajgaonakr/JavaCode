package batch65.java.programs;

public class GlobalVariables {

	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static boolean b1;
	static String s1;
	
	public static void main(String[] args) {
		
		System.out.println("Default value of global variable of type byte is : " + b);
		System.out.println("Default value of global variable of type short is : " + s);
		System.out.println("Default value of global variable of type int is : " + i);
		System.out.println("Default value of global variable of type long is : " + l);
		System.out.println("Default value of global variable of type float is : " + f);
		System.out.println("Default value of global variable of type double is : " + d);
		System.out.println("Default value of global variable of type boolean is : " + b1);
		System.out.println("Default value of global variable of type String is : " + s1);
	}
}
